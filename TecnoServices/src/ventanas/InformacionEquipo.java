    public InformacionEquipo() {
        initComponents();
        user = Login.user;
        IDequipo = InformacionCliente.IDequipo;
        IDcliente_update = GestionarClientes.IDcliente_update;
        
        try {
            Connection cn = Conexion.conectar();
            PreparedStatement pst = cn.prepareStatement(
                "select nombre_cliente from clientes where id_cliente = '" + IDcliente_update + "'");
            
            ResultSet rs = pst.executeQuery();
            
            if (rs.next()) {
                nom_cliente = rs.getString("nombre_cliente");
                
            }
            
        } catch (SQLException e) {
            System.err.println("Error al consultar el nombre del cliente");
        }
        txtNombreCliente.setText(nom_cliente);
        
        try {
            Connection cn = Conexion.conectar();
            PreparedStatement pst = cn.prepareStatement(
                "select * from equipos where id_equipo = '" + IDequipo + "'");
            
            ResultSet rs = pst.executeQuery();
            
            if (rs.next()) {
                
                cmbTipoEquipo.setSelectedItem(rs.getString("tipo_equipo"));
                cmbMarca.setSelectedItem(rs.getString("marca"));
                cmbEstatus.setSelectedItem(rs.getString("estatus"));
                txtModelo.setText(rs.getString("modelo"));
                txtNumSerie.setText(rs.getString("num_serie"));
                txtUltimaModificacion.setText(rs.getString("ultima_modificacion"));
                
                String dia = "", mes = "", annio = "";
                dia = rs.getString("dia_ingreso");
                mes = rs.getString("mes_ingreso");
                annio = rs.getString("annio_ingreso");
                txtFecha.setText(dia + " del " + mes + " del " + annio);
                
                txtpObservaciones.setText(rs.getString("observaciones"));
                txtpComentariosTecnicos.setText(rs.getString("comentarios_tecnicos"));
                labelrevisiontecnicade.setText("Comentarios y actualización del técnico: " + rs.getString("revision_tecnica_de"));
            }
            
        } catch (SQLException e) {
            System.err.println("Error al consultar la información del equipo " + e);
        }
        
        setTitle("Equipo del cliente " + nom_cliente);
        setSize(670, 530);
        setResizable(false);
        setLocationRelativeTo(null);
        
        setDefaultCloseOperation(WindowConstants.DISPOSE_ON_CLOSE);
        
        ImageIcon wallpaper = new ImageIcon("src/images/wallpaperPrincipal.jpg");
        Icon icono = new ImageIcon(wallpaper.getImage().getScaledInstance(labelWallpaper.getWidth(),
                labelWallpaper.getHeight(), Image.SCALE_DEFAULT));
        labelWallpaper.setIcon(icono);
        this.repaint();
        
        
    }
    
