    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed

        int validacion = 0;
        String tipo_equipo, marca, modelo, num_serie, estatus, observaciones;
        
        tipo_equipo = cmbTipoEquipo.getSelectedItem().toString();
        marca = cmbMarca.getSelectedItem().toString();
        estatus = cmbEstatus.getSelectedItem().toString();
        
        modelo = txtModelo.getText().trim();
        num_serie = txtNumSerie.getText().trim();
        observaciones = txtpObservaciones.getText();
        
        if (modelo.equals("")) {
            
            txtModelo.setBackground(Color.red);
            validacion++;
        }
        if (num_serie.equals("")) {
            
            txtNumSerie.setBackground(Color.red);
            validacion++;
        }
        if (observaciones.equals("")) {
            txtpObservaciones.setText("Sin Observaciones.");
        }
        
        if (validacion == 0) {
            
            try {
                Connection cn = Conexion.conectar();
                PreparedStatement pst = cn.prepareStatement(
                        "update equipos set tipo_equipo=?, marca=?, modelo=?, num_serie=?, observaciones=?, estatus=?, ultima_modificacion=? "
                                + "where id_equipo = '" + IDequipo + "'");
                
                pst.setString(1, tipo_equipo);
                pst.setString(2, marca);
                pst.setString(3, modelo);
                pst.setString(4, num_serie);
                pst.setString(5, observaciones);
                pst.setString(6, estatus);
                pst.setString(7, user);
                
                pst.executeUpdate();
                cn.close();
                
                Limpiar();
                
                txtNombreCliente.setBackground(Color.green);
                txtModelo.setBackground(Color.green);
                txtNumSerie.setBackground(Color.green);
                txtFecha.setBackground(Color.green);
                txtUltimaModificacion.setText(user);
                
                JOptionPane.showMessageDialog(null, "Actualización correcta.");
                this.dispose();
                
            } catch (SQLException e) {
                System.err.println("Error al actualizar datos del equipo. " + e);
                JOptionPane.showMessageDialog(null, "!!Error al actualizar datos del equipo¡¡ contacte al administrador.");
            }
            
        } else {
            JOptionPane.showMessageDialog(null, "Debes de llenar todos los campos.");
        }
    }//GEN-LAST:event_btnActualizarActionPerformed

